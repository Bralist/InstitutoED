    private void getPDF(){
        String directorio = txtDirectorio.getText();
        ImageIcon icon = new ImageIcon("src/main/resources/img/confirm32.png");
        String fecha = new Date().toString();
        int rpt = JOptionPane.showConfirmDialog(null, "Confirmar: Â¿Desea generar PDF?",
                    "Confirmar accion", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, icon);
        if (rpt == 0) {            
            MyTemplatePDF plantilla = new MyTemplatePDF(txtDirectorio.getText(), fecha, pruebas);
            plantilla.crearPlantilla();
            abrirPDF(txtDirectorio.getText());            
            this.dispose();
        }
    }
    
    public void abrirPDF(String director){
        try{
            File path = new File("src\\main\\resources\\exportpdf\\"+director + ".pdf");
            Desktop.getDesktop().open(path);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex, "Atencion", 2);
        }
    }

